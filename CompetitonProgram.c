task autonomous()
{
	stopTask(autonomousLCD);

	if(autoHolder == 0){
		startTask(right_center);
	}
	else if(autoHolder == 1){
		startTask(left_center);
	}
	else if(autoHolder == 2){
		startTask(right_3stars);
	}
	else if(autoHolder == 3){
		startTask(left_3stars);
	}
	else if(autoHolder == 4){
		startTask(Right_OneSide);
	}
	else if(autoHolder == 5){
		startTask(Left_OneSide);
	}
}

