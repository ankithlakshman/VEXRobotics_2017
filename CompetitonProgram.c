void pre_auton()
{
	clearLCDLine(0);
	clearLCDLine(1);
	startTask(autonomousLCD);

	//SET UP ALL SENSORS
	SensorType[in1] = sensorNone;
	SensorType[in2] = sensorNone;
	SensorType[in8] = sensorNone;
	wait1Msec(1000);
	SensorType[in1] = sensorPotentiometer;
	SensorType[in2] = sensorPotentiometer;
	SensorType[in8] = sensorGyro;


	SensorValue[in1] = 0;
	SensorValue[in2] = 0;
	SensorValue[in8] = 0;
	wait1Msec(2000);

	SensorScale[in8] = 260;
	SensorFullCount[in8] = 3600;


	//set up encoders
	SensorValue(rightEncoder) = 0;
	SensorValue(leftEncoder) = 0;

}

