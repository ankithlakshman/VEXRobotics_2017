task usercontrol()
{

	startTask(drive);
	startTask(lifting);
	startTask(clawSpeedTask);
	while (true)
	{
		liftCount = SensorValue(lift);

		clawCount = SensorValue(claw);

		limitSwitch = SensorValue(liftLimit);

		rightDriveCount = SensorValue(rightEncoder);

		leftDriveCount = SensorValue(leftEncoder);
	}

}
